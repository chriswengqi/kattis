int main()
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;
    ll ans = 0;

    while (n != 1)
    {
        ans += n;
        if (n % 2 == 0)
        {
            n = n / 2;
        }
        else
        {
            n = 3 * n + 1;
        }
    }

    cout << ans + 1 << endl;

    return 0;
}
