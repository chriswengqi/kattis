const double EPS = 1e-9;

int gauss(vector<vector<double>> a, vector<double> &ans)
{
    int n = (int)a.size();
    int m = (int)a[0].size() - 1;

    vector<int> where(m, -1);
    for (int col = 0, row = 0; col < m && row < n; ++col)
    {
        int sel = row;
        for (int i = row; i < n; ++i)
            if (abs(a[i][col]) > abs(a[sel][col]))
                sel = i;
        if (abs(a[sel][col]) < EPS)
            continue;
        for (int i = col; i <= m; ++i)
            swap(a[sel][i], a[row][i]);
        where[col] = row;

        for (int i = 0; i < n; ++i)
            if (i != row)
            {
                double c = a[i][col] / a[row][col];
                for (int j = col; j <= m; ++j)
                    a[i][j] -= a[row][j] * c;
            }
        ++row;
    }

    ans.assign(m, 0);
    for (int i = 0; i < m; ++i)
        if (where[i] != -1)
            ans[i] = a[where[i]][m] / a[where[i]][i];
    for (int i = 0; i < n; ++i)
    {
        double sum = 0;
        for (int j = 0; j < m; ++j)
            sum += ans[j] * a[i][j];
        if (abs(sum - a[i][m]) > EPS)
            return 0;
    }

    for (int i = 0; i < m; ++i)
        if (where[i] == -1)
            return INF;
    return 1;
}

void solve()
{
    int n, m;
    cin >> n >> m;
    vvi adj(n, vi());
    for (int i = 0; i < m; i++)
    {
        int a, b;
        cin >> a >> b;
        adj[a].pb(b);
        adj[b].pb(a);
    }

    vvd lhs(n - 1, vd(n, 0));
    for (int i = 0; i < n - 1; i++)
    {
        int sz = adj[i].size();
        lhs[i][i] = 1;
        for (int v : adj[i])
        {
            if (v != n - 1)
                lhs[i][v] = -1.0 / (double)sz;
        }
        lhs[i][n - 1] = 1;
    }
    vd rhs;

    gauss(lhs, rhs);
    cout << fixed << setprecision(10) << rhs[0] << endl;
}

